// JavaScript Document

// FOUND VIDEO IN HTML CODE //////////////////////////////////////////////////
var vid = document.getElementById("myVideo");
// ///////////////////////////////////////////////////////////////////////////

// START FUNCTION ////////////////////////////////////////////////////////////
window.onload = function() {
	vid.play();
	setTimeout(function() {pauseVid();}, 3500);
	setTimeout(function() {show1();}, 3500);
};
// ///////////////////////////////////////////////////////////////////////////

// STEP 1 FUNCTION ///////////////////////////////////////////////////////////
function step1() {
    vid.play();
	document.getElementById('stp1').style.display = 'none';
	setTimeout(function() {pauseVid();}, 8600);
	setTimeout(function() {show2();}, 8600);
}
// ///////////////////////////////////////////////////////////////////////////

// STEP 2 FUNCTION ///////////////////////////////////////////////////////////
function step2() {
    vid.play();
	document.getElementById('stp2').style.display = 'none';
	setTimeout(function() {pauseVid();}, 3000);
	setTimeout(function() {show3();}, 3000);
}
// ///////////////////////////////////////////////////////////////////////////

// STEP 3 FUNCTION ///////////////////////////////////////////////////////////
function step3() {
    vid.play();
	document.getElementById('stp3').style.display = 'none';
}
// ///////////////////////////////////////////////////////////////////////////


// SHOW SWIPE BOX FUNCTIONS //////////////////////////////////////////////////
function show1() {document.getElementById('stp1').style.display = 'block';}
function show2() {document.getElementById('stp2').style.display = 'block';}
function show3() {document.getElementById('stp3').style.display = 'block';}
// ///////////////////////////////////////////////////////////////////////////

// PAUSE VIDEO FUNCTION //////////////////////////////////////////////////////
function pauseVid() {
    vid.pause();
}
// ///////////////////////////////////////////////////////////////////////////


// STEP 1 SWIPE CONTROL //////////////////////////////////////////////////////
var el = document.getElementById('stp1');
swipedetect(el, function(swipedir) {if (swipedir == 'up') {step1();}});
// ///////////////////////////////////////////////////////////////////////////

// STEP 2 SWIPE CONTROL //////////////////////////////////////////////////////
var el = document.getElementById('stp2');
swipedetect(el, function(swipedir) {if (swipedir == 'left') {step2();}});
// ///////////////////////////////////////////////////////////////////////////

// STEP 3 SWIPE CONTROL //////////////////////////////////////////////////////
var el = document.getElementById('stp3');
swipedetect(el, function(swipedir) {if (swipedir == 'left') {step3();}});
// ///////////////////////////////////////////////////////////////////////////